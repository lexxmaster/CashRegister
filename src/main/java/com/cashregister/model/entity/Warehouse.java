package com.cashregister.model.entity;

public class Warehouse extends Entity{
    private String name;

    public Warehouse(long id, String name) {
        super(id);
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
